#!/usr/bin/env node
import * as cdk from 'aws-cdk-lib/core';
import { ENV } from '../lib/config';
import { NetworkStack } from '../lib/network-stack';
import { SessionSlicesStack } from '../lib/session-slices-stack';
import { AuthUserPoolStack } from '../lib/auth-user-pool-stack';
import { KnowledgeGraphDbStack } from '../lib/knowledge-graph-db-stack';
import { PhoenixStack } from '../lib/phoenix-stack';
import { BackendServiceStack } from '../lib/backend-service-stack';
import { FrontendSiteStack } from '../lib/frontend-site-stack';
import { DnsStack } from '../lib/dns-stack';
import { DevBoxStack } from '../lib/dev-box-stack';

const app = new cdk.App();

// Deploys to the account from the ambient CDK CLI credentials, us-east-1 to stay
// colocated with the rest of the PRAXIS stack (see lib/config.ts). Account is
// left undefined at synth time when CDK_DEFAULT_ACCOUNT is unset, so `cdk synth`
// works with no creds.
const env = ENV;

// Shared VPC consumed by the VPC-bound stacks (Phoenix, KG DB). Declared first
// so the cross-stack reference makes those stacks deploy after it. Per-stack
// physical-name / CIDR defaults now live in lib/config.ts; pass `-c key=value`
// to override.
const network = new NetworkStack(app, 'PraxisNetworkStack', { env });

new SessionSlicesStack(app, 'PraxisSessionSlicesStack', {
  env,
  bucketName: app.node.tryGetContext('sliceBucketName'),
  insightsTableName: app.node.tryGetContext('insightsTableName'),
  sliceRetentionDays: app.node.tryGetContext('sliceRetentionDays'),
});

new AuthUserPoolStack(app, 'PraxisAuthUserPoolStack', {
  env,
  userPoolName: app.node.tryGetContext('authUserPoolName'),
});

const knowledgeGraphDb = new KnowledgeGraphDbStack(app, 'PraxisKnowledgeGraphDbStack', {
  env,
  vpc: network.vpc,
  databaseName: app.node.tryGetContext('databaseName'),
  allowedCidr: app.node.tryGetContext('allowedCidr'),
});

const dns = new DnsStack(app, 'PraxisDnsStack', {
  env,
  domainName: app.node.tryGetContext('domainName') ?? 'praxiskg.com',
});

new PhoenixStack(app, 'PraxisPhoenixStack', {
  env,
  vpc: network.vpc,
  imageTag: app.node.tryGetContext('phoenixImageTag'),
  domain: app.node.tryGetContext('phoenixDomain') ?? 'phoenix.praxiskg.com',
  hostedZone: dns.zone,
  allowedWebCidr: app.node.tryGetContext('phoenixAllowedWebCidr'),
  dataVolumeGib: app.node.tryGetContext('phoenixDataVolumeGib'),
});

// The backend reads the RDS master secret at runtime, so the DB (and its
// secret) must exist before App Runner starts the container. No CDK reference
// couples them, so declare the ordering explicitly — otherwise `cdk deploy
// --all` may launch the service before the DB and the container fails to
// stabilize on its health check.
const backendService = new BackendServiceStack(app, 'PraxisBackendServiceStack', { env });
backendService.addDependency(knowledgeGraphDb);

// Default: served on CloudFront's autogenerated *.cloudfront.net URL (default
// cert, any region, no DNS). Pass `-c frontendDomain=app.example.com` to front
// it with a custom domain — that path needs the DnsStack zone and a us-east-1
// ACM cert.
const frontendDomain = app.node.tryGetContext('frontendDomain');
new FrontendSiteStack(app, 'PraxisFrontendSiteStack', {
  env,
  domainName: frontendDomain,
  hostedZone: frontendDomain ? dns.zone : undefined,
});

// Remote dev box for long-running Claude Code sessions over SSH (PyCharm). CPU
// by default; flip to a GPU by passing `-c devBoxInstanceType=g5.xlarge` (plus a
// driver-equipped `-c devBoxAmiId=...`) — no code change. Deploy on its own:
// `npx cdk deploy PraxisDevBoxStack -c devBoxAllowedSshCidr=<your-ip>/32 \
//    -c devBoxSshPublicKey="$(cat ~/.ssh/id_ed25519.pub)"`.
new DevBoxStack(app, 'PraxisDevBoxStack', {
  env,
  vpc: network.vpc,
  instanceType: app.node.tryGetContext('devBoxInstanceType'),
  allowedSshCidr: app.node.tryGetContext('devBoxAllowedSshCidr'),
  sshPublicKey: app.node.tryGetContext('devBoxSshPublicKey'),
  rootVolumeGib: app.node.tryGetContext('devBoxRootVolumeGib'),
  workspaceVolumeGib: app.node.tryGetContext('devBoxWorkspaceVolumeGib'),
  amiId: app.node.tryGetContext('devBoxAmiId'),
});
