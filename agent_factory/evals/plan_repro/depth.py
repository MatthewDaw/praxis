"""Depth scoring for the planning eval — the second question of the redesign.

Q1 (breadth, scored by coverage.run_coverage over the EXPLICIT golden): did the plan capture every
feature the raw PRD explicitly asked for?

Q2 (depth, THIS module): did the plan SURFACE the implied needs the planning lenses raise for each
capability? The implied set is GENERATED by applying each Praxis planning lens to the candidate plan —
never matched against a golden. A lens that applies to a capability whose implied need is neither
included nor flagged-as-a-decision is a DEPTH HOLE (the SMTP-class failure). "Surfaced" is satisfied
either by including the need OR by flagging it as an open/forced decision (raise-to-user / default).

The model is injected as ``Complete = (prompt) -> text`` (reuse evals.plan_repro.claude_cli) so this is
offline-testable and runs on the subscription with no API key.
"""
from __future__ import annotations

import json
import re
from collections.abc import Callable
from dataclasses import dataclass

Complete = Callable[[str], str]

SURFACED = "surfaced"   # implied need is included in the plan as a feature / made decision
FLAGGED = "flagged"     # raised explicitly as an open / forced decision (a fork for the user)
MISSING = "missing"     # neither included nor flagged -> a DEPTH HOLE
_STATUSES = (SURFACED, FLAGGED, MISSING)


def build_depth_prompt(lens: str, candidate) -> str:
    listing = "\n".join(f"  - {getattr(f, 'text', f)}" for f in candidate)
    return (
        "You are a DEPTH AUDITOR for a software plan. A planning LENS names a consideration a complete "
        "plan must address. Find EVERY capability in the CANDIDATE PLAN that the lens genuinely APPLIES "
        "to, and for each, judge whether the plan SURFACES the implied need the lens raises:\n"
        '  - "surfaced": the implied need is already included in the plan as a feature or a made decision.\n'
        '  - "flagged": the plan explicitly raises it as an open / forced decision (a fork for the user).\n'
        '  - "missing": the lens applies but the implied need is NEITHER included NOR flagged — a DEPTH HOLE.\n'
        "Report only capabilities the lens genuinely applies to. If it applies to nothing in this plan, "
        "return an empty list. Do not invent applicability.\n\n"
        f"LENS:\n  {lens}\n\n"
        f"CANDIDATE PLAN:\n{listing}\n\n"
        'Respond with JSON only: {"applies":[{"capability":"<plan capability the lens applies to>",'
        '"implied_need":"<what the lens requires for it>","status":"surfaced|flagged|missing",'
        '"evidence":"<short quote or reason>"}]}'
    )


def _loads(text: str) -> dict:
    s = (text or "").strip()
    if s.startswith("```"):
        s = re.sub(r"^```[a-zA-Z0-9_-]*\n?", "", s)
        s = re.sub(r"\n?```$", "", s).strip()
    try:
        return json.loads(s)
    except Exception:
        m = re.search(r"\{.*\}", s, re.S)
        try:
            return json.loads(m.group(0)) if m else {"applies": []}
        except Exception:
            return {"applies": []}


def _short(lens: str, n: int = 46) -> str:
    return (lens[:n] + "…") if len(lens) > n else lens


@dataclass
class DepthItem:
    lens: str
    capability: str
    implied_need: str
    status: str
    evidence: str = ""


@dataclass
class DepthReport:
    items: list  # list[DepthItem]

    @property
    def holes(self) -> list:
        return [i for i in self.items if i.status == MISSING]

    @property
    def passed(self) -> bool:
        return not self.holes

    def counts(self) -> dict:
        c = {s: 0 for s in _STATUSES}
        for i in self.items:
            c[i.status] = c.get(i.status, 0) + 1
        return c

    def format(self) -> str:
        c = self.counts()
        lines = [
            f"depth (implied-need surfacing): {c[SURFACED]} surfaced, {c[FLAGGED]} flagged, "
            f"{c[MISSING]} MISSING (of {len(self.items)} applicable) -> "
            f"{'PASS' if self.passed else 'FAIL'}"
        ]
        if self.holes:
            lines.append(f"  DEPTH HOLES ({len(self.holes)}) — implied needs neither built nor flagged:")
            for i in self.holes:
                lines.append(f"    - [{_short(i.lens)}] {i.capability} -> needs: {i.implied_need}")
        return "\n".join(lines)


def run_depth(complete: Complete, lenses: list[str], candidate) -> DepthReport:
    """One judge call per lens over the whole plan (cheap: O(lenses), not O(features×lenses))."""
    items: list[DepthItem] = []
    for lens in lenses:
        data = _loads(complete(build_depth_prompt(lens, candidate)))
        for a in (data.get("applies") or []):
            st = str(a.get("status", MISSING)).lower()
            if st not in _STATUSES:
                st = MISSING
            items.append(DepthItem(
                lens=lens, capability=str(a.get("capability", "?")),
                implied_need=str(a.get("implied_need", "?")), status=st,
                evidence=str(a.get("evidence", "")),
            ))
    return DepthReport(items=items)
